    module Templates =
        let private tryGetTemplateText (templateName: string) =
            let assembly = Assembly.GetExecutingAssembly()

            assembly.GetManifestResourceNames()
            |> Array.tryFind (fun name -> name.EndsWith(sprintf "templates.%s" templateName, StringComparison.Ordinal))
            |> Option.bind (fun resourceName ->
                use stream = assembly.GetManifestResourceStream(resourceName)

                if isNull stream then
                    None
                else
                    use reader = new StreamReader(stream)
                    Some(resourceName, reader.ReadToEnd()))

        let renderOrRaise (templateName: string) (model: (string * obj) list) =
            match tryGetTemplateText templateName with
            | None -> raise (TemplateRenderException(sprintf "Template not found: %s" templateName))
            | Some(path, text) ->
                let template = Template.Parse(text, path)

                if template.HasErrors then
                    let messages = template.Messages |> Seq.map string |> String.concat "; "

                    raise (TemplateRenderException(sprintf "Template parse failed for %s: %s" templateName messages))

                let context = TemplateContext()
                let scriptObject = ScriptObject()

                model |> List.iter (fun (name, value) -> scriptObject.Add(name, value))

                context.PushGlobal(scriptObject)
                template.Render(context)

