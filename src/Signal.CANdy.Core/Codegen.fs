                let preambleLines = System.Collections.Generic.List<string>()
                let structFieldLines = System.Collections.Generic.List<string>()
                let switchOpt2, _, branches2 = partitionMultiplex message

                let isMux2 =
                    match switchOpt2, branches2 with
                    | Some _, _ :: _ -> true
                    | _ -> false

                if isMux2 then
                    let enumName = sprintf "%s_mux_e" message.Name
                    preambleLines.Add(sprintf "typedef enum { ")

                    let enumEntries =
                        branches2
                        |> List.map (fun (k, _) -> sprintf "    %s_MUX_%d = %d" (message.Name.ToUpperInvariant()) k k)
                        |> String.concat ",\n"

                    preambleLines.Add enumEntries
                    preambleLines.Add(sprintf "} %s;" enumName)
                    preambleLines.Add ""

                    message.Signals
                    |> List.iteri (fun idx s ->
                        preambleLines.Add(
                            sprintf
                                "#define %s (%s << %d)"
                                (sprintf "%s_VALID_%s" (message.Name.ToUpperInvariant()) (s.Name.ToUpperInvariant()))
                                shiftSuffix
                                idx
                        ))

                    preambleLines.Add ""

                    structFieldLines.Add(sprintf "    %s valid;" validType)

                    if validType = "uint64_t" then
                        structFieldLines.Add("    /* valid field widened to uint64_t: signal count > 32 */")
                        structFieldLines.Add("    /* decode init literal: = 0ULL; */")

                    structFieldLines.Add(sprintf "    %s_mux_e mux_active;" message.Name)

                String.concat "\n" (List.ofSeq preambleLines), String.concat "\n" (List.ofSeq structFieldLines)

            let preStructDeclarations =
                [ valueTableDeclarations; muxPreambleDeclarations ]
                |> List.filter (fun block -> not (String.IsNullOrWhiteSpace block))
                |> String.concat "\n"

            let valueTableDefinitions =
                let lines = System.Collections.Generic.List<string>()

                let vtSignals =
                    message.Signals
                    |> List.choose (fun s -> s.ValueTable |> Option.map (fun vt -> s, vt))

                vtSignals
                |> List.iter (fun (s, vt) ->
                    lines.Add(sprintf "const char* %s_%s_to_string(int v) {" message.Name s.Name)
                    lines.Add "    switch (v) {"

                    vt
                    |> List.iter (fun (v, name) ->
                        lines.Add(sprintf "    case %d: return \"%s\";" v (name.Replace("\"", "\\\""))))

                    lines.Add "    default: return \"UNKNOWN\";"
                    lines.Add "    }"
                    lines.Add "}"
                    lines.Add "")

                String.concat "\n" (List.ofSeq lines)

            let crcSignalOpt = message.Signals |> List.tryFind (fun s -> s.CrcMeta.IsSome)

            let counterSignalOpt =
                message.Signals |> List.tryFind (fun s -> s.CounterMeta.IsSome)

            let hasCounter = counterSignalOpt.IsSome

            let counterStateTypeDecl =
                if hasCounter then
                    sprintf
                        "typedef struct { uint8_t last_counter; bool initialized; } %s_counter_state_t;"
                        message.Name
                else
                    ""

            let counterCheckFuncDecl =
                if hasCounter then
                    sprintf
                        "bool %s_check_counter(%s_counter_state_t* state, const %s_t* msg);"
                        message.Name
                        message.Name
                        message.Name
                else
                    ""

            let crcDecodeCheck =
                match crcSignalOpt with
                | Some crcSig when crcSig.CrcMeta.Value.Params.Width = 8 ->
                    let meta = crcSig.CrcMeta.Value

                    let funcName =
                        match meta.Algorithm with
                        | CrcAlgorithmId.CRC8_SAE_J1850 -> "sc_crc8_sae_j1850"
                        | CrcAlgorithmId.CRC8_8H2F -> "sc_crc8_8h2f"
                        | CrcAlgorithmId.Custom name -> sprintf "sc_crc8_%s" (name.ToLowerInvariant().Replace("-", "_"))
                        | _ -> "sc_crc8_unknown"

                    let byteCount = meta.ByteRange.End - meta.ByteRange.Start + 1

                    match message.CrcCounterMode with
                    | Some CrcCounterMode.Validate ->
                        sprintf
                            "    if (%s(&data[%d], %d) != (uint8_t)msg->%s) { return false; }"
                            funcName
                            meta.ByteRange.Start
                            byteCount
                            crcSig.Name
                    | _ -> ""
                | _ -> ""

            let crcEncodeInsert =
                match crcSignalOpt with
                | Some crcSig when crcSig.CrcMeta.Value.Params.Width = 8 ->
                    let meta = crcSig.CrcMeta.Value

                    let funcName =
                        match meta.Algorithm with
                        | CrcAlgorithmId.CRC8_SAE_J1850 -> "sc_crc8_sae_j1850"
                        | CrcAlgorithmId.CRC8_8H2F -> "sc_crc8_8h2f"
                        | CrcAlgorithmId.Custom name -> sprintf "sc_crc8_%s" (name.ToLowerInvariant().Replace("-", "_"))
                        | _ -> "sc_crc8_unknown"

                    let byteCount = meta.ByteRange.End - meta.ByteRange.Start + 1
                    let startEff = chooseStartBit crcSig config
                    let (_, setFn) = accessorNames crcSig.ByteOrder

                    match message.CrcCounterMode with
                    | Some CrcCounterMode.Validate ->
                        sprintf
                            "    uint8_t crc_val_%s = %s(&data[%d], %d);\n    %s(data, %d, %d, (uint64_t)crc_val_%s);"
                            crcSig.Name
                            funcName
                            meta.ByteRange.Start
                            byteCount
                            setFn
                            startEff
                            (int crcSig.Length)
                            crcSig.Name
                    | _ -> ""
                | _ -> ""

            let counterCheckFuncImpl =
                match counterSignalOpt with
                | Some counterSig ->
                    let meta = counterSig.CounterMeta.Value

                    sprintf
                        "bool %s_check_counter(%s_counter_state_t* state, const %s_t* msg) {\n    if (!state->initialized) { state->last_counter = (uint8_t)msg->%s; state->initialized = true; return true; }\n    uint8_t expected = (uint8_t)((state->last_counter + 1) %% %d);\n    state->last_counter = (uint8_t)msg->%s;\n    return (uint8_t)msg->%s == expected;\n}"
                        message.Name
                        message.Name
                        message.Name
                        counterSig.Name
                        meta.Modulus
                        counterSig.Name
                        counterSig.Name
                | None -> ""

            let headerContent =
                let model: (string * obj) list =
                    [ "banner", box banner
                      "header_guard", box (sprintf "%s_H" (message.Name.ToUpperInvariant()))
                      "pre_struct_declarations", box preStructDeclarations
                      "struct_extra_fields", box muxStructFields
                      "signal_declarations_h", box signalDeclarationsH
                      "message_name", box message.Name
                      "has_counter", box hasCounter
                      "counter_state_type_decl", box counterStateTypeDecl
                      "counter_check_func_decl", box counterCheckFuncDecl ]

                Templates.renderOrRaise "message.h.scriban" model

            let sourceContent =
                let model: (string * obj) list =
                    [ "banner", box banner
                      "message_header_name", box (sprintf "%s.h" messageNameLower)
                      "utils_header_name", box (Utils.utilsHeaderName config)
                      "value_table_definitions", box valueTableDefinitions
                      "message_name", box message.Name
                      "message_length", box (int message.Length)
                      "signal_decode_c", box signalDecodeC
                      "signal_encode_c", box signalEncodeC
                      "crc_decode_check", box crcDecodeCheck
                      "crc_encode_insert", box crcEncodeInsert
                      "has_counter", box hasCounter
                      "counter_check_func_impl", box counterCheckFuncImpl ]

                Templates.renderOrRaise "message.c.scriban" model

            File.WriteAllText(messageHPath, headerContent)
            File.WriteAllText(messageCPath, sourceContent)
            messageHPath, messageCPath

    module Registry =
        type private RegistryHeaderTemplateModel =
            { banner: string
              header_guard: string
              registry_declaration: string }

        type private RegistrySourceTemplateModel =
            { banner: string
              registry_header_name: string
              message_includes: string
              registry_body: string }

        let generateRegistryFiles (ir: Ir) (outputPath: string) (config: Signal.CANdy.Core.Config.Config) =
            let regHName = sprintf "%sregistry.h" config.FilePrefix
            let regCName = sprintf "%sregistry.c" config.FilePrefix
            let registryHPath = Path.Combine(outputPath, "include", regHName)
            let registryCPath = Path.Combine(outputPath, "src", regCName)

            let guard =
                (config.FilePrefix + "registry_h").ToUpperInvariant()
                |> Seq.map (fun ch -> if Char.IsLetterOrDigit ch then ch else '_')
                |> Seq.toArray
                |> fun arr -> new string (arr)

            let banner =
                sprintf
                    "/* Generated by Signal CANdy\n   file_prefix=%s, phys_type=%s, phys_mode=%s, dispatch=%s, motorola_start_bit=%s */\n"
                    config.FilePrefix
                    config.PhysType
                    config.PhysMode
                    config.Dispatch
                    config.MotorolaStartBit

            let registryHContent =
                let model: (string * obj) list =
                    [ "banner", box banner
                      "header_guard", box guard
                      "registry_declaration",
                      box "bool decode_message(uint32_t id, const uint8_t data[], uint8_t dlc, void* msg);" ]

                Templates.renderOrRaise "registry.h.scriban" model

            File.WriteAllText(registryHPath, registryHContent)

            let includes =
                ir.Messages
                |> List.map (fun m -> sprintf "#include \"%s.h\"" (m.Name.ToLowerInvariant()))
                |> String.concat "\n"

            let body =
                if config.Dispatch.ToLowerInvariant() = "direct_map" then
                    let cases =
                        ir.Messages
                        |> List.map (fun m ->
                            sprintf
                                "        case %du: return %s_decode((%s_t*)msg, data, dlc);"
                                (int m.Id)
                                m.Name
                                m.Name)
                        |> String.concat "\n"

                    sprintf
                        "bool decode_message(uint32_t id, const uint8_t data[], uint8_t dlc, void* msg) {\n    switch (id) {\n%s\n        default: return false;\n    }\n}"
                        cases
                else
                    let sorted = ir.Messages |> List.sortBy (fun m -> m.Id)

                    let entries =
                        sorted
                        |> List.map (fun m -> sprintf "    { %du, (decode_func_t)%s_decode }" (int m.Id) m.Name)
                        |> String.concat ",\n"

                    let table =
                        [ "typedef bool (*decode_func_t)(void* msg, const uint8_t data[], uint8_t dlc);"
                          ""
                          "typedef struct { uint32_t id; decode_func_t func; } decoder_entry_t;"
                          ""
                          "static const decoder_entry_t decoders[] = {"
                          entries
                          "};"
                          "" ]
                        |> String.concat "\n"

                    let search =
                        [ "bool decode_message(uint32_t id, const uint8_t data[], uint8_t dlc, void* msg) {"
                          "    int low = 0;"
                          "    int high = (int)(sizeof(decoders) / sizeof(decoder_entry_t)) - 1;"
                          "    while (low <= high) {"
                          "        int mid = low + (high - low) / 2;"
                          "        if (decoders[mid].id == id) {"
                          "            return decoders[mid].func(msg, data, dlc);"
                          "        }"
                          "        if (decoders[mid].id < id) low = mid + 1; else high = mid - 1;"
                          "    }"
                          "    return false;"
                          "}"
                          "" ]
                        |> String.concat "\n"

                    table + search

            let finalC =
                let model: (string * obj) list =
                    [ "banner", box banner
                      "registry_header_name", box (sprintf "%sregistry.h" config.FilePrefix)
                      "message_includes", box includes
                      "registry_body", box body ]

                Templates.renderOrRaise "registry.c.scriban" model

            File.WriteAllText(registryCPath, finalC)
            registryHPath, registryCPath

    // Compatibility shims for legacy includes (utils.h, registry.h)
    let private shimHeader (name: string) (target: string) =
        let guard = (name.Replace('.', '_') + "_SHIM").ToUpperInvariant()

        "#ifndef "
        + guard
        + "\n#define "
        + guard
        + "\n\n"
        + "#ifdef __cplusplus\nextern \"C\" {\n#endif\n\n"
        + "#include \""
        + target
        + "\"\n\n"
        + "#ifdef __cplusplus\n}\n#endif\n\n"
        + "#endif // "
        + guard

    // Main entry: generate code and return file lists
    let generate
        (ir: Ir)
        (outputPath: string)
        (config: Signal.CANdy.Core.Config.Config)
        : Result<GeneratedFiles, CodeGenError> =
        try
            // Ensure output directories
            Directory.CreateDirectory(Path.Combine(outputPath, "include")) |> ignore

            Directory.CreateDirectory(Path.Combine(outputPath, "src")) |> ignore

            let overflowGuard =
                ir.Messages
                |> List.tryFind (fun msg ->
                    let hasMuxSwitch =
                        msg.Signals |> List.exists (fun s -> s.MultiplexerIndicator = Some "M")

                    let hasMuxBranches =
                        msg.Signals
                        |> List.exists (fun s -> s.MultiplexerIndicator = Some "m" && s.MultiplexerSwitchValue.IsSome)

                    hasMuxSwitch && hasMuxBranches && msg.Signals.Length > 64)

            match overflowGuard with
            | Some msg ->
                Error(
                    CodeGenError.UnsupportedFeature(
                        sprintf
                            "Message '%s' has %d signals (>64); valid bitmask cannot be represented in 64 bits."
                            msg.Name
                            msg.Signals.Length
                    )
                )
            | None ->
                let ir = Signal.CANdy.Core.Dbc.applyConfigMetadata (Some config) ir

                let hasCrcValidateMode =
                    match config.CrcCounter with
                    | Some crcCfg when crcCfg.Mode = "validate" -> true
                    | _ -> false

                let crcCounterGuard =
                    match config.CrcCounter with
                    | Some crcCfg when crcCfg.Mode = "fail_fast" ->
                        if crcCfg.Messages.IsEmpty then
                            None
                        else
                            let firstMsg = crcCfg.Messages |> Map.toList |> List.head |> fst
                            Some(firstMsg, "CRC/counter", None)
                    | Some _ -> None
                    | None ->
                        if config.CrcCounterCheck then
                            ir.Messages
                            |> List.tryPick (fun msg ->
                                msg.Signals
                                |> List.tryFind (fun s -> s.IsCrc || s.IsCounter)
                                |> Option.map (fun s ->
                                    (msg.Name,
                                     (if s.IsCrc then "CRC"
                                      elif s.IsCounter then "counter"
                                      else "CRC/counter"),
                                     Some s.Name)))
                        else
                            None

                match crcCounterGuard with
                | Some(msgName, kind, signalNameOpt) ->
                    let signalInfo =
                        match signalNameOpt with
                        | Some signalName -> sprintf ", signal '%s'" signalName
                        | None -> ""

                    Error(
                        CodeGenError.UnsupportedFeature(
                            sprintf
                                "crc_counter_check=true: CRC/counter signal in message '%s' (%s%s) is not supported in the current configuration. Use crc_counter.mode=validate for explicit CRC validation."
                                msgName
                                kind
                                signalInfo
                        )
                    )
                | None ->
                    let hasAnyCrcMeta =
                        ir.Messages
                        |> List.exists (fun msg -> msg.Signals |> List.exists (fun s -> s.CrcMeta.IsSome))

                    let crcWidthError =
                        if hasCrcValidateMode || hasAnyCrcMeta then
                            ir.Messages
                            |> List.tryPick (fun msg ->
                                msg.Signals
                                |> List.tryPick (fun s ->
                                    match s.CrcMeta with
                                    | Some meta when meta.Params.Width <> 8 ->
                                        Some(
                                            sprintf
                                                "CRC signal '%s' in message '%s' has unsupported width %d (only CRC-8 is supported in this version)."
                                                s.Name
                                                msg.Name
                                                meta.Params.Width
                                        )
                                    | _ -> None))
                        else
                            None

                    match crcWidthError with
                    | Some errMsg -> Error(CodeGenError.UnsupportedFeature errMsg)
                    | None ->

                        // Clean stale prefixed common files
                        let keepUtilsH = Utils.utilsHeaderName config
                        let keepUtilsC = Utils.utilsSourceName config
                        let keepRegH = sprintf "%sregistry.h" config.FilePrefix
                        let keepRegC = sprintf "%sregistry.c" config.FilePrefix
                        let includeDir = Path.Combine(outputPath, "include")
                        let srcDir = Path.Combine(outputPath, "src")

                        if Directory.Exists includeDir then
                            Directory.GetFiles(includeDir, "*utils.h")
                            |> Array.iter (fun f ->
                                if Path.GetFileName(f) <> keepUtilsH then
                                    try
                                        File.Delete f
                                    with _ ->
                                        ())

                            Directory.GetFiles(includeDir, "*registry.h")
                            |> Array.iter (fun f ->
                                if Path.GetFileName(f) <> keepRegH then
                                    try
                                        File.Delete f
                                    with _ ->
                                        ())

                        if Directory.Exists srcDir then
                            Directory.GetFiles(srcDir, "*utils.c")
                            |> Array.iter (fun f ->
                                if Path.GetFileName(f) <> keepUtilsC then
                                    try
                                        File.Delete f
                                    with _ ->
                                        ())

                            Directory.GetFiles(srcDir, "*registry.c")
                            |> Array.iter (fun f ->
                                if Path.GetFileName(f) <> keepRegC then
                                    try
                                        File.Delete f
                                    with _ ->
                                        ())

                        // Generate utils
                        let uH = Utils.utilsHeaderName config
                        let uC = Utils.utilsSourceName config
                        let uHPath = Path.Combine(outputPath, "include", uH)
                        let uCPath = Path.Combine(outputPath, "src", uC)
                        File.WriteAllText(uHPath, Utils.utilsHContent config ir)
                        File.WriteAllText(uCPath, Utils.utilsCContent config ir)

                        // Emit compatibility shims
                        let shimUtilsPath = Path.Combine(outputPath, "include", "utils.h")
                        let shimRegPath = Path.Combine(outputPath, "include", "registry.h")
                        File.WriteAllText(shimUtilsPath, shimHeader "utils.h" uH)
                        File.WriteAllText(shimRegPath, shimHeader "registry.h" keepRegH)

                        // Messages
                        let msgFiles =
                            ir.Messages
                            |> List.map (fun m -> Message.generateMessageFiles m outputPath config)
                        // Registry
                        let regHPath, regCPath = Registry.generateRegistryFiles ir outputPath config

                        let sources = msgFiles |> List.map snd |> (fun xs -> uCPath :: regCPath :: xs)

                        let headers =
                            msgFiles
                            |> List.map fst
                            |> fun xs -> uHPath :: regHPath :: shimUtilsPath :: shimRegPath :: xs

                        let others: string list = []

                        Ok
                            { Sources = sources
                              Headers = headers
                              Others = others }
        with
        | TemplateRenderException msg -> Error(CodeGenError.TemplateError msg)
        | ex -> Error(CodeGenError.Unknown(sprintf "Codegen exception: %s" ex.Message))
