                  "        int curByte = byte;"
                  "        int curBit = bit - (int)i;"
                  "        while (curBit < 0) { curBit += 8; ++curByte; } // move to next higher byte"
                  "        uint8_t b = data[curByte];"
                  "        uint8_t bitval = (uint8_t)((b >> curBit) & 1u);"
                  "        value = (value << 1) | bitval; // assemble MSB-first"
                  "    }"
                  "    return value;"
                  "}"
