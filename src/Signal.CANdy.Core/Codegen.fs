        // Helper to choose C accessor based on byte order
        let accessorNames (byteOrder: ByteOrder) =
            match byteOrder with
            | ByteOrder.Little -> ("get_bits_le", "set_bits_le")
            | ByteOrder.Big -> ("get_bits_be", "set_bits_be")

        // Convert Motorola (BE) start bit from LSB-convention to MSB-convention using sawtooth numbering.
        let internal motorolaMsbFromLsb (start: int) (length: int) : int =
            let steps = max 0 (length - 1)
            let mutable byteIdx = start / 8
            let mutable bitIdx = start % 8 // 0..7, where 7 is MSB

            for _ in 1..steps do
                if bitIdx < 7 then
                    bitIdx <- bitIdx + 1
                else
                    (byteIdx <- byteIdx - 1
                     bitIdx <- 0)

            byteIdx * 8 + bitIdx

        // Choose effective start bit depending on config for Motorola signals; LE stays unchanged.
        let chooseStartBit (signal: Signal) (config: Signal.CANdy.Core.Config.Config) : int =
            let start = int signal.StartBit
            let len = int signal.Length

            match signal.ByteOrder with
            | ByteOrder.Big ->
                match config.MotorolaStartBit.ToLowerInvariant() with
                | "lsb" -> motorolaMsbFromLsb start len
                | _ -> start // default "msb"
            | _ -> start

        // Detect if factor equals 10^-n within tolerance and return integer scale (10^n)
        let tryPowerOfTenScale (factor: float) : int64 option =
            if factor <= 0.0 then
                None
            else
                let eps = 1e-12

                let rec loop n =
                    if n > 9 then
                        None
                    else
                        let scaleF = pown 10.0 n

                        if abs (factor - (1.0 / scaleF)) < eps then
                            Some(int64 (pown 10 n))
                        else
                            loop (n + 1)

                loop 0

        /// Detect when DBC [min|max] fields store raw CAN counts instead of physical values.
        /// Returns true if the declared min/max range cannot contain the full physical range
        /// computed from factor, offset, and bit width - indicating raw-count sentinel usage.
        let isRawRangeSentinel
            (minV: float)
            (maxV: float)
            (factor: float)
            (offset: float)
            (length: uint16)
            (isSigned: bool)
            : bool =
            let bitLen = int length
            let unsignedRawMax = Math.Pow(2.0, float bitLen) - 1.0
            let signedHalf = Math.Pow(2.0, float (bitLen - 1))
            let signedRawMin = -signedHalf
            let signedRawMax = signedHalf - 1.0

            let rawMin, rawMax =
                if isSigned then
                    (signedRawMin, signedRawMax)
                else
                    (0.0, unsignedRawMax)

            let physMin = offset + factor * rawMin
            let physMax = offset + factor * rawMax
            let eps = 1e-9
            let physOutOfDeclaredRange = physMin < minV - eps || physMax > maxV + eps

            let matchesUnsignedRawCountRange =
                abs minV <= eps && abs (maxV - unsignedRawMax) <= eps

            let matchesSignedRawCountRange =
                isSigned && abs (minV - signedRawMin) <= eps && abs (maxV - signedRawMax) <= eps

            let matchesSignedUnsignedPhysicalRange =
                isSigned
                && abs (minV - offset) <= eps
                && abs (maxV - (offset + factor * unsignedRawMax)) <= eps

            physOutOfDeclaredRange
            && (matchesUnsignedRawCountRange
                || matchesSignedRawCountRange
                || matchesSignedUnsignedPhysicalRange)

